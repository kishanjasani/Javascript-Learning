// Simple print to console.
console.log( 'Hello World!' );

var name = 'Kishan';

// String concat
var lastName = 'Jasani'
console.log( 'Hello ' + name + ' ' + lastName );

// Number
var age = 23;
console.log( age );

// Boolean
var fullAge = true;
console.log( fullAge );

var job, isMarried;
